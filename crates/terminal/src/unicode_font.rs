//! Extended Unicode bitmap font — 8x16 glyphs beyond ASCII.
//!
//! Provides `get_glyph(char) -> &[u8; 16]` with coverage for:
//! - ASCII 32–126 (delegated to terminus.rs)
//! - Latin-1 Supplement (U+00A0–U+00FF): accented characters, symbols
//! - Box Drawing (U+2500–U+257F): TUI borders and connectors
//! - Block Elements (U+2580–U+259F): progress bars, fills
//! - Arrows (U+2190–U+2193): directional indicators
//! - Common symbols: bullets, check/cross marks, math operators
//! - Greek letters commonly used in technical text
//!
//! Each glyph is 8 pixels wide x 16 pixels tall, stored as 16 bytes
//! (1 byte per scanline row, MSB = leftmost pixel). Same format as Terminus.
//!
//! Fallback: characters without a glyph render as a dotted box (U+FFFD style).

use super::terminus::{self, CHAR_HEIGHT};

/// Replacement glyph for unmapped characters — a dotted rectangle suggesting
/// "unknown character" without being as heavy as a solid block.
static REPLACEMENT: [u8; CHAR_HEIGHT] = [
    0x00, 0x00, 0xFE, 0x82, 0x82, 0x82, 0xAA, 0x82,
    0xAA, 0x82, 0x82, 0xFE, 0x00, 0x00, 0x00, 0x00,
];

/// Look up the bitmap for any character. Fallback chain:
/// 1. ASCII 32–126 → terminus glyphs
/// 2. Extended Unicode → this module's tables
/// 3. Everything else → replacement glyph
#[inline]
pub fn get_glyph(c: char) -> &'static [u8; CHAR_HEIGHT] {
    let cp = c as u32;

    // ASCII range — delegate to terminus (the fast path)
    if cp >= 32 && cp <= 126 {
        return terminus::get_glyph(c);
    }

    // Latin-1 Supplement (U+00A0–U+00FF)
    if cp >= 0xA0 && cp <= 0xFF {
        return &LATIN1_SUPPLEMENT[(cp - 0xA0) as usize];
    }

    // Arrows (U+2190–U+2193)
    if cp >= 0x2190 && cp <= 0x2193 {
        return &ARROWS[(cp - 0x2190) as usize];
    }

    // Box Drawing (U+2500–U+257F)
    if cp >= 0x2500 && cp <= 0x257F {
        return &BOX_DRAWING[(cp - 0x2500) as usize];
    }

    // Block Elements (U+2580–U+259F)
    if cp >= 0x2580 && cp <= 0x259F {
        return &BLOCK_ELEMENTS[(cp - 0x2580) as usize];
    }

    // Miscellaneous symbols — sparse lookup
    match cp {
        0x2022 => return &SYMBOL_BULLET,        // •
        0x25CF => return &SYMBOL_BLACK_CIRCLE,   // ●
        0x25CB => return &SYMBOL_WHITE_CIRCLE,   // ○
        0x25A0 => return &SYMBOL_BLACK_SQUARE,   // ■
        0x25A1 => return &SYMBOL_WHITE_SQUARE,   // □
        0x25AA => return &SYMBOL_SM_BLACK_SQ,    // ▪
        0x25AB => return &SYMBOL_SM_WHITE_SQ,    // ▫
        0x2605 => return &SYMBOL_BLACK_STAR,     // ★
        0x2606 => return &SYMBOL_WHITE_STAR,     // ☆
        0x2713 => return &SYMBOL_CHECK,          // ✓
        0x2717 => return &SYMBOL_CROSS,          // ✗
        0x00B1 => return &LATIN1_SUPPLEMENT[0xB1 - 0xA0], // ± (in Latin-1)
        0x00D7 => return &LATIN1_SUPPLEMENT[0xD7 - 0xA0], // × (in Latin-1)
        0x00F7 => return &LATIN1_SUPPLEMENT[0xF7 - 0xA0], // ÷ (in Latin-1)
        // Greek letters
        0x03B1 => return &GREEK_ALPHA,   // α
        0x03B2 => return &GREEK_BETA,    // β
        0x03B3 => return &GREEK_GAMMA,   // γ
        0x03B4 => return &GREEK_DELTA,   // δ
        0x03B5 => return &GREEK_EPSILON, // ε
        0x03C0 => return &GREEK_PI,      // π
        0x03C3 => return &GREEK_SIGMA,   // σ
        0x03A9 => return &GREEK_OMEGA,   // Ω
        0x03BC => return &GREEK_MU,      // μ
        0x03BB => return &GREEK_LAMBDA,  // λ
        _ => {}
    }

    &REPLACEMENT
}

