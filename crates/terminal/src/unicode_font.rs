#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn ascii_delegates_to_terminus() {
        // ASCII characters should return the same pointer as terminus
        for c in ' '..='~' {
            let ours = get_glyph(c) as *const _;
            let theirs = terminus::get_glyph(c) as *const _;
            assert_eq!(ours, theirs, "Glyph mismatch for ASCII {:?}", c);
        }
    }

    #[test]
    fn latin1_supplement_coverage() {
        // All Latin-1 Supplement chars should return non-replacement glyphs
        for cp in 0xA0u32..=0xFF {
            let c = char::from_u32(cp).unwrap();
            let glyph = get_glyph(c);
            let repl_ptr = &REPLACEMENT as *const _;
            let glyph_ptr = glyph as *const _;
            assert_ne!(glyph_ptr, repl_ptr, "U+{:04X} should have a glyph", cp);
        }
    }

    #[test]
    fn box_drawing_coverage() {
        // All box drawing chars should return non-replacement glyphs
        for cp in 0x2500u32..=0x257F {
            let c = char::from_u32(cp).unwrap();
            let glyph = get_glyph(c);
            let repl_ptr = &REPLACEMENT as *const _;
            let glyph_ptr = glyph as *const _;
            assert_ne!(glyph_ptr, repl_ptr, "U+{:04X} should have a glyph", cp);
        }
    }

    #[test]
    fn block_elements_coverage() {
        for cp in 0x2580u32..=0x259F {
            let c = char::from_u32(cp).unwrap();
            let glyph = get_glyph(c);
            let repl_ptr = &REPLACEMENT as *const _;
            let glyph_ptr = glyph as *const _;
            assert_ne!(glyph_ptr, repl_ptr, "U+{:04X} should have a glyph", cp);
        }
    }

    #[test]
    fn unknown_returns_replacement() {
        // A CJK character we definitely don't have
        let glyph = get_glyph('\u{4E00}');
        let repl_ptr = &REPLACEMENT as *const _;
        let glyph_ptr = glyph as *const _;
        assert_eq!(glyph_ptr, repl_ptr);
    }

    #[test]
    fn check_mark_has_glyph() {
        let glyph = get_glyph('\u{2713}');
        let repl_ptr = &REPLACEMENT as *const _;
        let glyph_ptr = glyph as *const _;
        assert_ne!(glyph_ptr, repl_ptr);
    }

    #[test]
    fn arrows_have_glyphs() {
        for c in ['\u{2190}', '\u{2191}', '\u{2192}', '\u{2193}'] {
            let glyph = get_glyph(c);
            let repl_ptr = &REPLACEMENT as *const _;
            let glyph_ptr = glyph as *const _;
            assert_ne!(glyph_ptr, repl_ptr, "Arrow {:?} should have a glyph", c);
        }
    }

    #[test]
    fn greek_letters_have_glyphs() {
        for c in ['\u{03B1}', '\u{03B2}', '\u{03C0}', '\u{03A9}'] {
            let glyph = get_glyph(c);
            let repl_ptr = &REPLACEMENT as *const _;
            let glyph_ptr = glyph as *const _;
            assert_ne!(glyph_ptr, repl_ptr, "Greek {:?} should have a glyph", c);
        }
    }

    #[test]
    fn glyph_size_is_correct() {
        // Every glyph should be exactly CHAR_HEIGHT bytes
        let g = get_glyph('\u{2500}');
        assert_eq!(g.len(), CHAR_HEIGHT);
    }
}
